public class homework3 {
    public static void main(String[] args) {

        yildiz(7);
    }
    public static void yildiz (int number){
        for (int i=1;i<=number;i++){
            for (int j=1;j<=i;j++){
                System.out.print( "*");

            }
            System.out.println();
        }


    }
}
